    	<h1 class="text-center">Culture in Srilanka</h1><br>Museums and theatres - Currently four national museums and 26 archaeological museums have been established in Sri Lanka. National museums are maintained by the Department of National Museums and archaeological museums by the Department of Archaeology. Beside the museums a large number of theatres also can be found in Sri Lanka.<br>

Festivals - Sri Lanka is a multi-cultural country with several different festivals celebrated by various communities. Sinhala and Tamil New Year celebrations, Vesak Festival, Christmas, Ramadan festival, Thai Pongal, and the Galle Literary Festival are a few of the major festivals.<br>

Performing arts - There are three main traditional dance forms in Sri Lanka: Kandyan dancing, low country dancing, and Sabaragamuwa dancing. Though not unique to Sri Lanka, 'Bharatanatyam', which originated from India is also popular in Sri Lanka particularly among the Tamil community.<br>

Food - The cuisine of Sri Lanka has been influenced by many historical, cultural, and other factors. Rice is the main staple diet of the country. Other staples include hoppers, string hoppers, and pittu.<br>

Being one of the largest producers of tea in the world Sri Lanka is best known for the production of unorthodox tea. Tea was introduced to the country by the British who called the country "Ceylon". Pure Ceylon tea is considered some of the finest tea produced anywhere in the world.<br>
Handicrafts - Handicrafts available in Sri Lanka include wood carving, silverware, brass castings, ceramic ware, bamboo products, pottery, batiks, lace works, cane works, costume jewelry, lacquerware, wooden masks, coir goods, handlooms, and ivory products.


